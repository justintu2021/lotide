const findKey = function(obj,callback) {
  let key = Object.keys(obj)  /// list of name ["Blue Hill", "Akaleri", .... ]
  let result;
  
  for (let i of key) {        /// i = "Blue Hill" or "Akaleri" .... 
    if(callback(obj[i])) {
      return result = i
      break;
    }
  }
return undefined  
}



